
public class Cidade {
	
	private int CCod;
	private String CNome;
	
	
	public Cidade(int cCod, String cNome) {
		CCod = cCod;
		CNome = cNome;
	}
	
	public int getCCod() {
		return CCod;
	}
	public void setCCod(int cCod) {
		CCod = cCod;
	}
	public String getCNome() {
		return CNome;
	}
	public void setCNome(String cNome) {
		CNome = cNome;
	}
	
	
}
